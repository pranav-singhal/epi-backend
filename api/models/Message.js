    type: { type: 'string' },

    sender: { type: 'string' },

    recipient: { type: 'string' },

    transactionId: { type: 'number' }

    //  ╔═╗╔╦╗╔╗ ╔═╗╔╦╗╔═╗
    //  ║╣ ║║║╠╩╗║╣  ║║╚═╗
